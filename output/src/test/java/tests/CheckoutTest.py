from playwright.sync_api import Page, expect

def test_checkout(page: Page):
    page.goto("https://store.steampowered.com/")
    page.hover(".tab_content.browse_tab")
    page.click('text="Video Games"')
    page.click('text="Action"')
    page.click('text="Far Cry 6"')
    page.click('text="Add to Cart"')
    page.click('text="View Cart"')
    page.click('text="Remove"')
    page.click('text="Add to Cart"')
    page.select_option('select[name="Country"]', "Canada")
    page.click('text="Checkout"')
    page.fill('input[name="first_name"]', "Name")
    page.fill('input[name="last_name"]', "Last Name")
    page.fill('input[name="phone"]', "123412341")
    page.fill('input[name="address1"]', "1234 Address St")
    page.fill('input[name="city"]', "Highlands")
    page.select_option('select[name="state"]', "BC")
    page.fill('input[name="zip"]', "V9B 0K7")
    page.fill('input[name="email"]', "")
    expect(page).to_have_enabled_selector('button[name="continue_btn"]')